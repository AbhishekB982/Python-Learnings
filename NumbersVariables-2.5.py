##2.5.	Circle Calculations - Find the area and circumference of a circle given its radius.

pi = 3.14159

radius = float(input("Enter the radius of the circle: "))

circumferenece = 2 * pi * radius
area_of_circle = pi * radius * radius

print("The circumference of the circle is: ", circumferenece)
print("The area of circle is: ", area_of_circle)